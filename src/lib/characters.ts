// =============================
// キャラクター定義 — 8種類×5段階進化
// =============================

export type CharacterType = "cat" | "dog" | "rabbit" | "fox" | "bear" | "panda" | "lion" | "frog";

export interface CharacterStage {
  name: string;       // 段階名
  emoji: string;      // フォールバック絵文字
  minXp: number;      // この段階に必要な累計XP
  title: string;      // 称号
  description: string; // 説明
}

export interface CharacterDef {
  type: CharacterType;
  nameJa: string;
  /** 12×16 pixel art grid. Each char = color index (0=transparent, 1-9=palette) */
  pixels: string[];
  /** Color palette per stage [stage0…stage4], each array has colors for indices 1-9 */
  palettes: string[][];
  stages: CharacterStage[];
}

// =============================
// 進化段階 XP しきい値 (共通)
// =============================
const STAGE_XP = [0, 2000, 8000, 20000, 50000];

function makeStages(names: string[], emojis: string[], titles: string[], descs: string[]): CharacterStage[] {
  return names.map((name, i) => ({
    name,
    emoji: emojis[i],
    minXp: STAGE_XP[i],
    title: titles[i],
    description: descs[i],
  }));
}

