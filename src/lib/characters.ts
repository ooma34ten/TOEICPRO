/** XPからキャラクターの進化段階 (0-4) を返す */
export function getStageIndex(totalXp: number): number {
  for (let i = STAGE_XP.length - 1; i >= 0; i--) {
    if (totalXp >= STAGE_XP[i]) return i;
  }
  return 0;
}

/** 次の段階までに必要なXPを返す (最終段階ならnull) */
export function getXpToNextStage(totalXp: number): { current: number; needed: number; nextStageXp: number } | null {
  const stage = getStageIndex(totalXp);
  if (stage >= STAGE_XP.length - 1) return null;
  const nextXp = STAGE_XP[stage + 1];
  const currentStageXp = STAGE_XP[stage];
  return {
    current: totalXp - currentStageXp,
    needed: nextXp - currentStageXp,
    nextStageXp: nextXp,
  };
}

/** キャラクタータイプから定義を取得 */
export function getCharacterDef(type: CharacterType): CharacterDef {
  return CHARACTER_DEFS.find(c => c.type === type) || CHARACTER_DEFS[0];
}

